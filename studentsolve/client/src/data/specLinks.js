export { IGCSE_SPEC_LINKS, A_LEVEL_SPEC_LINKS };

export function getSpecUrl(subject, board, qualification) {
  if (!subject || !board) return null;
  if (qualification === 'IGCSE') return IGCSE_SPEC_LINKS[subject]?.[board] || null;
  if (qualification === 'A-Level' || qualification === 'AS-Level') return A_LEVEL_SPEC_LINKS[subject]?.[board] || null;
  return SPEC_LINKS[subject]?.[board] || null; // default GCSE
}